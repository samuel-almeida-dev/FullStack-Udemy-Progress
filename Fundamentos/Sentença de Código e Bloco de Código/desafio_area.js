/*
    Desenvolva um script em JavaScript que calcule a área de um círculo. 
    O script deve aceitar o raio do círculo como entrada e retornar a área calculada. 
    Lembre-se, a fórmula para calcular a área de um círculo é (área = π * r²) é o raio do círculo. 
*/

{
    let raio = 4;
    let areaDoCirculo = Math.PI * (raio * raio);
    console.log(`A área do círculo é ${areaDoCirculo.toFixed(2)} cm².`);
}

