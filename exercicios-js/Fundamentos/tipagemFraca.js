let qualquer = 'Legal';
console.log(qualquer);
console.log(typeof qualquer); // é uma string;

qualquer = 3.42;
console.log(qualquer);
console.log(typeof qualquer); // é um number;

/*
    - Tipos de dados: Três tipos básicos, numbers, boolean e strings;
    - typeof retorna o tipo da variável, se for um número é number, string é string e assim por diante;
    - JavaScript é uma linguagem de tipagem fraca, por isso permite essa alteração do tipo de dado em variável;
    - Evite nomes genéricos ou siglas em variáveis;
*/

